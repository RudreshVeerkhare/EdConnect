package com.oop.edconnect;

public class Student {
    String name;
    String username;

    public Student(String name, String username) {
        this.name = name;
        this.username = username;
    }

    public Student() {

    }

    public String getName() {
        return name;
    }

    public String getUsername() {
        return username;
    }
}
